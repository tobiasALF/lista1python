ladoA = int(input("Digite o valor do lado A do retângulo em metros: "))
ladoB = int(input("Digite o valor do lado B do retângulo em metros: "))

area = ladoA * ladoB
perimetro = 2 * ladoA + 2 * ladoB

print("Área do retângulo:", area, "metros quadrados")
print("Perímetro do retângulo:", perimetro, "metros")